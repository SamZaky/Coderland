        <h1  class="fronth1">Front-End Developer</h1>
        <p>Front-end developers are the people who build websites. They are responsible for the design, layout, and the way content appears on a webpage. This is done in a number of ways, but the most popular front-end languages are HTML, CSS, and JavaScript.</p>
        <p>Some of the main responsibilities of a front-end web developer include:</p>
        <ul>
            <li><p>Taking design and turning it into a website by writing hundreds of lines of code (trust me, it’s more fun than it sounds!).</p></li>
            <li><p>Making sure that the website in question is functional and interactive as required. This includes doing things like inserting animations, creating hyperlinked buttons, and more.</p></li>
            <li><p>Minimizing page load speed times and maximizing page responsiveness. Things like this play a major role in a website’s search engine optimization (SEO) score, and it is important to get them right.</p></li>
            <p>As you can see, a front-end web developer has a lot of roles. However, they are mainly quite simple and easy to learn. Unfortunately, front-end developers are probably among the lowest paid specialists who know what is coding and programming – but don’t let this put you off!</p>
        </ul>
    </div>

        <h1 class="fronth1">The Languages You Need To Know As a Front-End Developer :</h1>

    <div class="presentation">
           <img src="../public/assets/img/html.png" class="logo">
           <p>HTML – or Hypertext Markup Language – is probably the most widespread coding language, and is probably the one which comes to the average person’s mind when they are asked the question “What is coding?”. HTML is used in front end of internet development, and it is found on pretty much every single website on the entire internet, making it an essential language for any front-end developer to use.</p>
        <p>As far as learning a bit of coding background goes, HTML is a great place to start. It is a very simple language, it offers a quite basic syntax, and it is easy to understand. When I asked you to take a look at the page source code at the start of this article, what most of what you saw was HTML. It tells your web browser where to put things, what those things are, and how those things should look – among other things</p>
           <p>If you want to start the journey to a front-end web developer, then you should start with HTML.</p>
       </div>

    <div class="presentation">
        <img src="../public/assets/img/css.png" class="logo">
        <p>CSS is the second of the front-end development languages, after HTML. It is also quite easy to learn, although not as easy as HTML is. A lot of people choose to learn HTML and CSS at the same time, as you really need both before you can do a whole lot.</p>
        <p>Using CSS alongside HTML allows you to style your webpage by customizing things like fonts, colors, styles, and more. While the HTML tells your web browser what a certain part of your web page should look like, the CSS code is what actually controls it.</p>
        <p>As we noted earlier, a lot of people choose to learn HTML and CSS alongside each other. They are both relatively simple, and they can both be used to style and manipulate a webpage.</p>
    </div>

    <div class="presentation">
        <img src="../public/assets/img/js.png" class="logo">
        <p>The last language on our list, JavaScript (not to be confused with Java), has been one of the fastest growing languages over the past few years. The increasing demand for JavaScript developers has led to a major shortage, making it a language of choice for many new programmers.</p>
        <p>JavaScript has traditionally been used for front-end website development to create interactive displays. It controls things like video players, animations, and GIFs. However, in recent times JavaScript has been used more and more for back-end development, meaning that you can theoretically develop the majority of your website using the one language.</p>
        <p>Like the other languages discussed above, JavaScript is a good choice if you are just starting out on your programming journey. It is relatively simple, and there is a wide range of resources out here for learners to draw on.</p>

